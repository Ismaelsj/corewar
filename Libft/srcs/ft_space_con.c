#include "ft_printf.h"

int		ft_space_con(t_flag *tmp)
{
	if (tmp->space > -1 && tmp->minus == -1 && tmp->plus == -1
		&& (ft_decimal(tmp->specifier) == 1))
		return (1);
	return (0);
}
