#include "ft_printf.h"

char	*ft_put_two(wint_t c)
{
	char	str[3];

	str[0] = 0b11000000 | ((c & 0b11111000000) >> 6);
	str[1] = 0b10000000 | (c & 0b111111);
	str[2] = '\0';
	return (ft_strdup(str));
}

char	*ft_put_three(wint_t c)
{
	char	str[4];

	str[0] = 0b11100000 | ((c & 0b1111000000000000) >> 12);
	str[1] = 0b10000000 | ((c & 0b111111000000) >> 6);
	str[2] = 0b10000000 | (c & 0b111111);
	str[3] = '\0';
	return (ft_strdup(str));
}

char	*ft_put_four(wint_t c)
{
	char	str[5];

	str[0] = 0b11110000 | ((c & 0b111000000000000000000) >> 18);
	str[1] = 0b10000000 | ((c & 0b1111110000000000) >> 12);
	str[2] = 0b10000000 | ((c & 0b11111100000) >> 6);
	str[3] = 0b10000000 | (c & 0b111111);
	str[4] = '\0';
	return (ft_strdup(str));
}
